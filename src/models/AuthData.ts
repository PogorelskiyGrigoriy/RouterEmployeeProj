/**
 * @module AuthData
 * Модели данных для системы аутентификации и авторизации.
 */

/**
 * Данные, передаваемые пользователем при попытке входа.
 */
export type LoginData = {
    /** Электронная почта пользователя (используется как логин) */
    email: string;
    /** Пароль в открытом виде (перед отправкой на сервер) */
    password: string;
}

/**
 * Доступные роли пользователей в системе.
 * Используется для разграничения прав доступа (RBAC).
 */
export type UserRole = 'ADMIN' | 'USER';

/**
 * Данные авторизованного пользователя, полученные с сервера.
 */
export type UserData = {
    /** Уникальный идентификатор пользователя из базы данных */
    id: string;
    /** Отображаемое имя пользователя */
    username: string;
    /** Системная роль пользователя для проверки прав */
    role: UserRole;
}